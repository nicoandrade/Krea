/**
 * Assign Menus, Front adn Blog page
 */
function krea_after_import_setup( $import_files ) {

    // Assign menus to their locations.
    $main_menu = get_term_by( 'name', 'Main', 'nav_menu' );
    $footer_menu = get_term_by( 'name', 'Footer', 'nav_menu' );
    $social_menu = get_term_by( 'name', 'Social', 'nav_menu' );

    set_theme_mod( 'nav_menu_locations', array(
            'primary' => $main_menu->term_id,
            'footer-menu' => $footer_menu->term_id,
            'social' => $social_menu->term_id,
        )
    );

    // Assign front page and posts page (blog page).
    $front_page_id = get_page_by_title( 'Shop' );
    $shop_page_id = get_page_by_title( 'Shop' );
    $blog_page_id  = get_page_by_title( 'Blog' );
    $woocommerce_cart_page_id = get_page_by_title( 'Cart' );
    $woocommerce_checkout_page_id = get_page_by_title( 'Checkout' );
    $woocommerce_myaccount_page_id = get_page_by_title( 'My Account' );

    switch ( $import_files['import_file_name'] ) {

        case 'Default':

            break;

        case 'Home Big':

            break;

        case 'Home Slider':
            $front_page_id = get_page_by_title( 'Home Slider' );
            break;

        case 'Home Horizontal':
            $front_page_id = get_page_by_title( 'Home Horizontal' );
            break;

        case 'Home Stairs':
            $front_page_id = get_page_by_title( 'Home Stairs' );
            break;

        case 'Home Triple Full':
            $front_page_id = get_page_by_title( 'Triple Full' );
            break;

        case 'Home Full Slider':
            $front_page_id = get_page_by_title( 'Home Full Slider' );
            break;

        case 'Catalog':

            break;

        case 'Banners Shop':
            $front_page_id = get_page_by_title( 'Banners Shop' );
            break;

        case 'Jewelry Stairs':
            $front_page_id = get_page_by_title( 'Stairs Shop' );
            break;

        case 'Fashion':
            
            break;

        case 'Fashion Slider':
            $front_page_id = get_page_by_title( 'Home Slider' );
            break;
    }

    update_option( 'show_on_front', 'page' );

    //Front Page and Blog Page
    update_option( 'page_on_front', $front_page_id->ID );
    update_option( 'page_for_posts', $blog_page_id->ID );

    //WooCommerce
    update_option( 'woocommerce_shop_page_id', $shop_page_id->ID );
    update_option( 'woocommerce_cart_page_id', $woocommerce_cart_page_id->ID );
    update_option( 'woocommerce_checkout_page_id', $woocommerce_checkout_page_id->ID );
    update_option( 'woocommerce_myaccount_page_id', $woocommerce_myaccount_page_id->ID );



}
add_action( 'pt-ocdi/after_import', 'krea_after_import_setup' );


